class MultiFlavorForm extends React.Component {
  constructor(props) {
    super(props);
    this.state = {value: ['coconut', 'lime']};

    this.handleChange = this.handleChange.bind(this);
    this.handleSubmit = this.handleSubmit.bind(this);
  }

  handleChange(event) {
    const selected = Array.apply(null, event.target.options).filter((e) => e.selected).map((e)=> e.value);
    // const selected = event.target.options.filter((e) => e.selected).map((e)=> e.value);
    this.setState({value: selected});
  }

  handleSubmit(event) {
    alert('Your favorite flavor is: ' + this.state.value);
    event.preventDefault();
  }

  render() {
    return (
      <form onSubmit={this.handleSubmit}>
        <label>
          Pick your favorite flavors:
          <select multiple={true} value={this.state.value} onChange={this.handleChange}>
            <option value="grapefruit">Grapefruit</option>
            <option value="lime">Lime</option>
            <option value="coconut">Coconut</option>
            <option value="mango">Mango</option>
          </select>
        </label>
        <input type="submit" value="Submit" />
      </form>
    );
  }
}

